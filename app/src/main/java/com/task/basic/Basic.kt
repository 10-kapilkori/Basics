package com.task.basic

fun main() {
//    Primitive data types :-
    val num: Int = 10
    val floatPoint: Float = 10.3f
    val doublePoint: Long = 10L
    val characters: Char = 'a'
    val conditions: Boolean = false

    println(
        "Num in Int: $num\nFloatPoint in Float: $floatPoint\nDoublePoint in Double: $doublePoint\n" +
                "Characters in Char: $characters\nConditions in Boolean: $conditions"
    )
}